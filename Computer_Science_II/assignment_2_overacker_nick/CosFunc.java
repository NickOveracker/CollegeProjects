public class CosFunc extends Function
{
   /**
    * Returns cos(x).
    *
    * @param x The position at which to evaluate cosine.
    * @return cosin(x)
    */
   public double evaluate(double x)
   {
      return Math.cos(x);
   } // end evaluate()

} // end class CosFunc
