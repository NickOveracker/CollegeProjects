public class SinFunc extends Function
{
   /**
    * Returns sin(x).
    *
    * @param x The position at which to evaluate sine.
    * @return sin(x)
    */
   public double evaluate(double x)
   {
      return Math.sin(x);
   } // end evaluate()

} // end class SinFunc
